let currentLang = 'ru';

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['ru'][key] || key;
}

function setLang(lang) {
  currentLang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerHTML = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
  document.documentElement.lang = lang;
  // re-render dynamic content
  if (typeof renderMarket === 'function') {
    renderMarket();
    updateMarketSortHeaders();
  }
  if (document.getElementById('page-guide')?.classList.contains('active') && typeof renderGuide === 'function') renderGuide();
  if (document.getElementById('page-compare')?.classList.contains('active') && typeof renderComparison === 'function') {
    renderComparison();
  }
  if (document.getElementById('page-calc')?.classList.contains('active') && typeof calc1Update === 'function') calc1Update();
  if (document.getElementById('page-rental')?.classList.contains('active') && typeof calcRental === 'function') calcRental();
}
